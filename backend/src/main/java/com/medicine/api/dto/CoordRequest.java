package com.medicine.api.dto;

public class CoordRequest {

    private String view;
    private int x;
    private int y;

    public String getView() {
        return view;
    }

    public void setView(String view) {
        this.view = view;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
